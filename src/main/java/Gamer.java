public class Gamer {
    private int xPointGamer;
    private int yPointGamer;


    public Gamer(int xPointGamer, int yPointGamer) {
        this.xPointGamer = xPointGamer;
        this.yPointGamer = yPointGamer;

    }

    public void setxPointGamer(int xPointGamer) {
        this.xPointGamer = xPointGamer;
    }

    public void setyPointGamer(int yPointGamer) {
        this.yPointGamer = yPointGamer;
    }

    public int getxPointGamer() {
        return xPointGamer;
    }

    public int getyPointGamer() {
        return yPointGamer;
    }
}
